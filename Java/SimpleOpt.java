public class SimpleOpt {
    //method overloading
    
    static int Add(int a, int b)
    {
        return a + b;
    }

    static int Add(int a, int b, int c)
    {
        return a + b + c;
    }

    static double Add(double a, double b)
    {
        return a + b;
    }

    static float Add(float a, float b)
    {
        return a + b;
    }
}
